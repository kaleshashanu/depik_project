int getDecimalInteger(void)
{
  int sum = 0;
  int ch = 0, flag=0;

  while(1)
  {
    ch = getchar();

    if((ch >= '0') && (ch <= '9'))
    {
      sum *= 10;
      sum = sum + (ch - '0');
      flag = 1;
    }
    if( (flag == 1) && ((ch < '0') || (ch >'9')) )
      break;
   }
   return sum;
}  
